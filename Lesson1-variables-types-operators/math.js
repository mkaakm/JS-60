// Math.floor() - Повертає найбільше ціле число, менше, або яке дорівнює зазначеному числу.
console.log(Math.floor(1.7))

//Math.ceil() - Повертає найменше ціле число, більше або яке дорівнює зазначеному числу
console.log(Math.ceil(1.2))

//Math.round() - Повертає значення числа, округденного до найближчого цілого.
console.log(Math.round(1.2))
console.log(Math.round(1.5))